# Map a marker on the 320x170 equirectangular world SVG back to lat/lon, so we
# can compare a simulated supplier's location against live earthquakes/weather.
def pixel_to_latlon(x, y):
    lon = (x / 320.0) * 360.0 - 180.0
    lat = 90.0 - (y / 170.0) * 180.0
    return lat, lon

