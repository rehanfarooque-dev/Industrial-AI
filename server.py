class Handler(SimpleHTTPRequestHandler):
    def __init__(self, *args, **kwargs):
        super().__init__(*args, directory=str(ROOT), **kwargs)

    def log_message(self, fmt, *args):
        print("  %s - %s" % (self.address_string(), fmt % args), flush=True)

    def _send_json(self, payload, status=200):
        body = json.dumps(payload).encode("utf-8")
        self.send_response(status)
        self.send_header("Content-Type", "application/json; charset=utf-8")
        self.send_header("Content-Length", str(len(body)))
        self.send_header("Access-Control-Allow-Origin", "*")
        self.send_header("Cache-Control", "no-store")
        self.end_headers()
        self.wfile.write(body)

    def do_GET(self):
        path = self.path.split("?", 1)[0].rstrip("/")

        if path == "/api/health":
            return self._send_json({"status": "ok", "time": time.time(),
                                    "live": live.status(), "anyLive": live.any_live()})

        if path == "/api/live":
            return self._send_json({
                "status": live.status(), "anyLive": live.any_live(),
                "quakes": (live.get_quakes() or [])[:8],
                "carbon": live.get_carbon(), "fx": live.get_fx(),
                "weather": live.get_weather(), "macro": live.get_macro(),
                "plant": live.PLANT,
            })

        if path == "/api/modules":
            return self._send_json({"modules": MODULES})

        if path == "/api/reseed":
            seed.main()
            return self._send_json({"status": "reseeded"})

        if path.startswith("/api/module/"):
            mid = path[len("/api/module/"):]
            if mid not in QUERIES:
                return self._send_json({"error": "unknown module '%s'" % mid}, 404)
            try:
                return self._send_json({"id": mid, "data": module_data(mid)})
            except Exception as e:  # surface query errors as JSON, don't 500 silently
                return self._send_json({"error": "%s: %s" % (type(e).__name__, e)}, 500)

        if path.startswith("/api/"):
            return self._send_json({"error": "not found"}, 404)

        if self.path in ("/", ""):
            self.path = "/index.html"
        return super().do_GET()


