def q_commercial(con):
    # --- High-value prioritization (real open tickets + value at risk) -----
    open_total = con.execute("SELECT COUNT(*) n FROM tickets WHERE status='open'").fetchone()["n"]
    pri = []
    for r in con.execute("""SELECT t.issue, t.severity, t.risk_score, t.value_at_risk,
                            c.name FROM tickets t JOIN customers c ON c.id=t.customer_id
                            WHERE t.status='open' ORDER BY t.risk_score DESC LIMIT 4"""):
        label = {"high": "High", "medium": "Medium", "low": "Low"}[r["severity"]]
        pri.append({"client": r["name"], "issue": r["issue"],
                    "riskScore": round(r["risk_score"]), "priority": r["severity"],
                    "label": label, "valueAtRisk": round(r["value_at_risk"]),
                    "sla": SLA_HOURS[r["severity"]]})

    # --- Support-copilot scenarios across ALL distinct issue types ---------
    # (one representative real account per issue, highest-value first)
    scenarios, seen = [], set()
    for r in con.execute("""SELECT t.issue, c.name, c.contact, t.value_at_risk FROM tickets t
                            JOIN customers c ON c.id=t.customer_id
                            ORDER BY t.value_at_risk DESC"""):
        if r["issue"] in seen:
            continue
        seen.add(r["issue"])
        script = COPILOT_SCRIPTS.get(r["issue"], DEFAULT_SCRIPT)
        scenarios.append({"title": r["issue"], "client": r["name"], "contact": r["contact"],
                          "messages": [{"role": role, "text": txt.replace("{client}", r["contact"])}
                                       for role, txt in script]})

    # --- Supplier risk: markers + ranked list, elevated by LIVE quakes -----
    quakes = live.get_quakes() or []
    elevated = 0
    markers, ranked = [], []
    for r in con.execute("""SELECT name, country, map_x, map_y, on_time_rate, risk_score
                            FROM suppliers"""):
        lat, lon = pixel_to_latlon(r["map_x"], r["map_y"])
        boost, near = 0.0, None
        for qk in quakes:
            dist = live.haversine_km(lat, lon, qk["lat"], qk["lon"])
            if dist < 2000 and qk["mag"]:
                b = qk["mag"] * 6 * (1 - dist / 2000)
                if b > boost:
                    boost, near = b, qk
        risk = clamp(r["risk_score"] + boost, 0, 100)
        if boost > 5:
            elevated += 1
        color = "#d8581f" if risk >= 60 else "#e7a93a" if risk >= 30 else "#5e9f55"
        band = "high" if risk >= 60 else "medium" if risk >= 30 else "low"
        reason = ("M%.1f quake nearby (live)" % near["mag"]) if (near and boost > 5) \
            else "%d%% on-time delivery" % round(r["on_time_rate"] * 100)
        markers.append({"x": r["map_x"], "y": r["map_y"], "color": color,
                        "name": r["name"], "country": r["country"],
                        "risk": round(risk), "band": band, "reason": reason})
        ranked.append({"name": r["name"], "country": r["country"], "risk": round(risk),
                       "band": band, "reason": reason})
    ranked.sort(key=lambda x: x["risk"], reverse=True)

    # --- Contract intelligence: flagged clauses (linked to supplier risk) --
    flags = []
    for i, s in enumerate(ranked):
        if s["risk"] >= 30:
            clause, lvl, excerpt = CLAUSE_CATALOG[i % len(CLAUSE_CATALOG)]
            flags.append({"supplier": s["name"], "clause": clause, "level": lvl,
                          "excerpt": excerpt})
    contracts = {"analyzed": len(ranked), "flagged": len(flags), "topFlags": flags[:4]}

    # --- Next-best-action: the highest-upside account ----------------------
    top = con.execute("""SELECT name, segment, arr, upsell_score
                         FROM customers ORDER BY upsell_score DESC LIMIT 1""").fetchone()
    uplift = clamp(round(top["upsell_score"] * 0.35), 5, 40)
    conf = clamp(round(60 + top["upsell_score"] * 0.35), 80, 99)
    days = clamp(round(2 + (100 - top["upsell_score"]) / 22), 2, 7)
    action = ("Propose premium service tier to %s (%s) based on usage and growth signals."
              % (top["name"], top["segment"]))
    return {
        "prioritization": {"cases": pri, "openTotal": open_total},
        "copilot": {"scenarios": scenarios},
        "supplierMarkers": markers,
        "supplierTop": ranked[:5],
        "supplierLive": {"quakes": len(quakes), "elevated": elevated, "live": bool(quakes)},
        "contracts": contracts,
        "insight": {"action": action, "arrUplift": "+%d%%" % uplift,
                    "confidence": "%d%%" % conf, "toClose": "%dd" % days},
    }


def q_plant(con):
    # Per-line availability from raw OEE shifts (drives per-asset utilization)
    line_avail = {}
    for r in con.execute("""SELECT line_id, (SUM(planned_min)-SUM(downtime_min))
                            *1.0/SUM(planned_min) a FROM oee_shifts GROUP BY line_id"""):
        line_avail[r["line_id"]] = r["a"]
    line_names = {r["id"]: r["name"] for r in con.execute("SELECT id, name FROM lines")}

    # All assets: reliability risk + utilization + status + detail (emergent)
    assets, worst = [], None
    for a in con.execute("""SELECT id, name, kind, line_id, age_hours,
                            weibull_shape, weibull_scale FROM assets"""):
        risk = 1.0 - math.exp(-((a["age_hours"] / a["weibull_scale"]) ** a["weibull_shape"]))
        if worst is None or risk > worst[0]:
            worst = (risk, a)
        # utilization = line availability, derated by this machine's wear
        util = clamp(round((line_avail.get(a["line_id"], 0.85)) * 100 * (1 - 0.45 * risk)), 8, 99)
        if risk >= 0.65:
            status, label, color = "maintenance", "Maintenance due", "#d8581f"
        elif util < 66:
            status, label, color = "idle", "Underutilized", "#e7a93a"
        else:
            status, label, color = "running", "Running", "#5e9f55"
        # per-asset remaining useful life (mean residual life)
        mlife = a["weibull_scale"] * math.gamma(1 + 1 / a["weibull_shape"])
        rul = int(clamp(round(mlife - a["age_hours"]), 0, 9999))
        rec = ("Schedule maintenance now — failure risk is high." if status == "maintenance"
               else "Rebalance load — this asset is underutilized." if status == "idle"
               else "Operating normally — no action needed.")
        assets.append({"name": a["name"], "kind": a["kind"], "utilization": util,
                       "status": status, "statusLabel": label, "color": color,
                       "line": line_names.get(a["line_id"], "—"),
                       "risk": round(risk * 100), "rul": rul,
                       "agePct": round(a["age_hours"] / a["weibull_scale"] * 100),
                       "recommendation": rec})
    assets.sort(key=lambda x: x["utilization"], reverse=True)
    overall_util = round(sum(x["utilization"] for x in assets) / (len(assets) or 1))
    counts = {"running": 0, "idle": 0, "maintenance": 0}
    for x in assets:
        counts[x["status"]] += 1

    # Predictive maintenance: the highest-risk asset
    risk, a = worst
    # Mean residual life: Weibull mean (scale * Gamma(1+1/shape)) minus current age.
    # Aged/over-life assets yield a small RUL => genuine maintenance urgency.
    mean_life = a["weibull_scale"] * math.gamma(1 + 1 / a["weibull_shape"])
    hours = int(clamp(round(mean_life - a["age_hours"]), 4, 720))

    # OEE from raw shift records: Availability x Performance x Quality
    o = con.execute("""SELECT SUM(planned_min) p, SUM(downtime_min) d,
                        SUM((planned_min-downtime_min)*ideal_rate) ideal,
                        SUM(good_units) g, SUM(scrap_units) s FROM oee_shifts""").fetchone()
    A = (o["p"] - o["d"]) / o["p"]
    produced = o["g"] + o["s"]
    P = clamp(produced / (o["ideal"] or 1), 0, 1)
    Q = o["g"] / (produced or 1)
    avail_loss = round((1 - A) * 100, 1)
    perf_loss = round(A * (1 - P) * 100, 1)
    qual_loss = round(A * P * (1 - Q) * 100, 2)
    actual = round(A * P * Q * 100, 1)

    # Energy: quarterly production volume vs unit utility cost (learning curve)
    eq = con.execute("SELECT quarter, units, cost_per_unit FROM energy_quarter ORDER BY quarter").fetchall()
    units = [r["units"] for r in eq]
    cpu = [r["cost_per_unit"] for r in eq]
    facility = [{"label": r["name"], "value": r["value"], "color": r["color"],
                 "icon": r["icon"]} for r in con.execute(
        "SELECT name, value, color, icon FROM facility_metrics ORDER BY sort")]
    # --- Overlay LIVE signals onto the facility gauges ---------------------
    carbon = live.get_carbon()
    if carbon and carbon.get("gco2") is not None:
        cidx = (carbon.get("index") or "").lower()
        ccolor = ("#5e9f55" if cidx in ("very low", "low") else
                  "#e7a93a" if cidx == "moderate" else "#d8581f")
        facility[1] = {"label": "Grid Carbon (live)", "value": str(carbon["gco2"]),
                       "color": ccolor, "icon": "\U0001F30D"}
    wx = live.get_weather()
    if wx and wx.get("temp_c") is not None:
        # HVAC load rises as ambient temp departs from a 21C setpoint
        load = clamp(40 + abs(wx["temp_c"] - 21) * 2.2, 30, 95)
        facility[0] = {"label": "HVAC Load (live %.0f°C)" % wx["temp_c"],
                       "value": "%.1f" % load, "color": "#1f4e9b", "icon": "\U0001F321"}
    return {
        "maintenance": {"hoursToFailure": hours, "risk": round(risk, 2),
                        "asset": a["name"], "kind": a["kind"]},
        "assetUtil": {"assets": assets, "overall": overall_util, "counts": counts},
        "oee": {"availabilityLoss": avail_loss, "performanceLoss": perf_loss,
                "qualityLoss": qual_loss, "actualOEE": actual},
        "energy": {"labels": ["Q1", "Q2", "Q3", "Q4"],
                   "production": [round(v) for v in scale_to(units, 85)],
                   "costPerUnit": [round(v) for v in scale_to(cpu, 85)]},
        "facility": facility,
    }


def q_supply(con):
    # Inventory: per-SKU days-of-cover -> two populations (stock-out vs excess)
    fast, slow = [], []
    for r in con.execute("SELECT on_hand, daily_demand, mover FROM skus"):
        cover = r["on_hand"] / (r["daily_demand"] or 1)
        (fast if r["mover"] == "fast" else slow).append(cover)
    stockout = scale_to(smooth(histogram(fast, 60, 0, 59)), 100)
    excess = scale_to(smooth(histogram(slow, 60, 0, 59)), 100)

    vendors = [{"name": r["name"], "trustScore": round(r["trust_score"]),
                "onTime": round(r["on_time_rate"] * 100), "quality": round(r["quality_rate"] * 100)}
               for r in con.execute("""SELECT name, trust_score, on_time_rate, quality_rate
                                       FROM vendors ORDER BY trust_score DESC""")]

    # Fleet: live vehicle/route status (delayed first)
    fleet = [{"vehicle": r["vehicle"], "route": r["route"], "status": r["status"],
              "eta": r["eta_hours"], "load": r["load_pct"], "reason": r["delay_reason"]}
             for r in con.execute("""SELECT vehicle, route, status, eta_hours, load_pct,
                                     delay_reason FROM shipments
                                     ORDER BY status='delayed' DESC, eta_hours DESC""")]
    on_time = sum(1 for f in fleet if f["status"] == "on-time")

    # Warehouse slotting zones (ABC) + derived KPIs
    zones = [{"zone": z["zone"], "klass": z["klass"], "utilization": z["utilization"],
              "picks": z["picks_per_hr"], "travel": z["travel_m"], "reslot": bool(z["reslot"])}
             for z in con.execute("""SELECT zone, klass, utilization, picks_per_hr,
                                     travel_m, reslot FROM wh_zones ORDER BY id""")]
    tot_picks = sum(z["picks"] for z in zones) or 1
    # pick efficiency: share of picks coming from low-travel (fast) zones
    fast_picks = sum(z["picks"] for z in zones if z["klass"] == "Fast")
    avg_travel = round(sum(z["travel"] * z["picks"] for z in zones) / tot_picks, 1)
    warehouse = {"zones": zones, "pickEfficiency": round(fast_picks / tot_picks * 100),
                 "avgTravel": avg_travel, "reslotSuggestions": sum(1 for z in zones if z["reslot"])}

    # Back-office automation: orders -> invoices. "Standard" orders (margin in the
    # normal band) auto-process straight-through; outliers need manual review.
    bo = con.execute("""SELECT COUNT(*) total,
                        SUM(CASE WHEN margin BETWEEN 28 AND 48 THEN 1 ELSE 0 END) auto
                        FROM orders""").fetchone()
    total_docs, auto_docs = bo["total"], (bo["auto"] or 0)
    auto_rate = round(auto_docs / (total_docs or 1) * 100)
    backoffice = {"documents": total_docs, "autoRate": auto_rate,
                  "manual": total_docs - auto_docs,
                  "hoursSaved": round(auto_docs * 4 / 60),   # ~4 min saved/doc
                  "steps": ["Data Entry", "Validation", "Document Generation"]}
    return {
        "inventory": {"labels": list(range(60)),
                      "stockoutRisk": stockout, "excessStock": excess},
        "vendors": vendors,
        "fleet": {"vehicles": fleet, "onTime": on_time, "delayed": len(fleet) - on_time,
                  "total": len(fleet)},
        "warehouse": warehouse,
        "backoffice": backoffice,
    }


def q_quality(con):
    # Quality distribution: deviation -> quality score histogram, rework cutoff
    scores = []
    for r in con.execute("SELECT deviation FROM inspections"):
        scores.append(clamp(round(60 - 2 * r["deviation"]), 0, 59))
    dist = scale_to(smooth(histogram(scores, 60, 0, 59), 2), 100)
    threshold = clamp(round(60 - 2 * 14), 0, 59)  # dev=14 => p(rework)=0.5

    # Defects: the most recent inspection that actually has bounding boxes
    insp = con.execute("SELECT MAX(inspection_id) id FROM defects").fetchone()
    boxes = []
    if insp and insp["id"] is not None:
        boxes = [{"x": r["x"], "y": r["y"], "w": r["w"], "h": r["h"], "kind": r["kind"]}
                 for r in con.execute(
                     "SELECT x,y,w,h,kind FROM defects WHERE inspection_id=?", (insp["id"],))]
    # overall scrap rate (share of inspections that needed rework) -> pass rate
    rw = con.execute("SELECT AVG(rework) r FROM inspections").fetchone()["r"] or 0
    pass_rate = round((1 - rw) * 100, 1)

    # Digital twin: latest sensor reading per metric
    twin = con.execute("SELECT id FROM assets WHERE is_twin=1 LIMIT 1").fetchone()["id"]
    dt = []
    fmt = {"Pressure": ("%d%%", 100.0), "Temperature": ("%d°C", 80.0),
           "Speed": ("%d RPM", 3500.0)}
    for metric in ("Pressure", "Temperature", "Speed"):
        v = con.execute("""SELECT value FROM sensor_readings WHERE asset_id=? AND metric=?
                           ORDER BY ts DESC LIMIT 1""", (twin, metric)).fetchone()["value"]
        f, vmax = fmt[metric]
        dt.append({"label": metric, "value": f % round(v),
                   "pct": clamp(round(v / vmax * 100), 5, 99)})

    safety = con.execute("SELECT incidents FROM safety_quarter ORDER BY quarter").fetchall()

    # Root cause: aggregate every defect by its 6M category (Method/Machine/Material)
    cause_map = {"scratch": "Material", "porosity": "Material", "dent": "Machine",
                 "crack": "Machine", "misalignment": "Method"}
    tally = {"Method": 0, "Machine": 0, "Material": 0}
    for r in con.execute("SELECT kind, COUNT(*) n FROM defects GROUP BY kind"):
        tally[cause_map.get(r["kind"], "Method")] += r["n"]
    tot = sum(tally.values()) or 1
    drivers = sorted(({"name": k, "pct": round(v / tot * 100)} for k, v in tally.items()),
                     key=lambda d: d["pct"], reverse=True)
    return {
        "qualityDist": {"labels": list(range(60)), "values": dist,
                        "reworkThreshold": threshold},
        "defects": {"boxes": boxes, "count": len(boxes),
                    "verdict": "FAIL" if boxes else "PASS", "passRate": pass_rate},
        "rootCause": {"drivers": drivers, "top": drivers[0]["name"], "total": tot},
        "digitalTwin": dt,
        "safety": {"labels": ["Q1", "Q2", "Q3", "Q4"],
                   "incidents": [r["incidents"] for r in safety]},
    }


