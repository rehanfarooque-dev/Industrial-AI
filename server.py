def q_productivity(con):
    run_c, change_c = "#16345f", "#e7a93a"
    schedule, order = {}, {}
    runmonths = {}
    for r in con.execute("""SELECT line_id, name, start_month, end_month, kind
                            FROM schedule_runs ORDER BY line_id, start_month"""):
        if r["name"] not in schedule:
            schedule[r["name"]] = []
            order[r["name"]] = r["line_id"]
            runmonths[r["name"]] = 0
        schedule[r["name"]].append({"s": r["start_month"], "e": r["end_month"],
                                    "kind": r["kind"],
                                    "c": run_c if r["kind"] == "run" else change_c})
        if r["kind"] == "run":
            runmonths[r["name"]] += r["end_month"] - r["start_month"]
    sched = [{"name": n, "segs": schedule[n], "runPct": round(runmonths[n] / 12 * 100)}
             for n in sorted(schedule, key=lambda x: order[x])]

    # Operator copilot scenarios from real machines (distinct kinds)
    ops, kinds_seen = [], set()
    for a in con.execute("SELECT name, kind FROM assets ORDER BY age_hours DESC"):
        if a["kind"] in kinds_seen:
            continue
        kinds_seen.add(a["kind"])
        symptom, steps = OPERATOR_STEPS.get(a["kind"], DEFAULT_OP)
        ops.append({"asset": a["name"], "kind": a["kind"],
                    "query": "Troubleshoot %s — %s" % (a["name"], symptom),
                    "steps": [{"n": i + 1, "text": t, "icon": ic}
                              for i, (t, ic) in enumerate(steps)]})
        if len(ops) >= 4:
            break

    # Engineering KB: rank documents per query by keyword + base relevance
    docs = [{"title": r["title"], "icon": r["icon"], "type": r["doc_type"],
             "rel": r["relevance"], "low": r["title"].lower()}
            for r in con.execute("SELECT title, icon, doc_type, relevance FROM kb_documents")]
    kb_queries = []
    for q, keys in KB_QUERIES:
        scored = []
        for d in docs:
            overlap = sum(1 for k in keys if k in d["low"])
            score = 0.45 * d["rel"] + 0.55 * (overlap / len(keys))
            scored.append((score, d))
        scored.sort(key=lambda s: s[0], reverse=True)
        results = [{"title": d["title"], "icon": d["icon"], "type": d["type"],
                    "match": int(clamp(round(sc * 100), 40, 99))}
                   for sc, d in scored[:3]]
        kb_queries.append({"q": q, "results": results})

    # AI service desk: real triage funnel from ticket statuses
    sd = {r["status"]: r["n"] for r in con.execute(
        "SELECT status, COUNT(*) n FROM tickets GROUP BY status")}
    incoming = sum(sd.values()) or 1
    resolved = sd.get("resolved", 0)
    serviceDesk = {"incoming": incoming, "triaged": sd.get("triaged", 0),
                   "resolved": resolved, "open": sd.get("open", 0),
                   "autoResolvePct": round(resolved / incoming * 100)}

    # Alert prioritization: a real cross-signal feed (assets, supply, inventory, live)
    alerts = []
    for r in con.execute("""SELECT name, kind, age_hours, weibull_shape, weibull_scale
                            FROM assets"""):
        risk = 1.0 - math.exp(-((r["age_hours"] / r["weibull_scale"]) ** r["weibull_shape"]))
        if risk >= 0.6:
            alerts.append({"level": "high" if risk >= 0.8 else "medium",
                           "title": "%s — failure risk %d%%" % (r["name"], round(risk * 100)),
                           "source": "Predictive Maintenance", "score": risk})
    low_cov = con.execute("""SELECT COUNT(*) n FROM skus
                             WHERE on_hand*1.0/daily_demand < lead_time_days""").fetchone()["n"]
    if low_cov:
        alerts.append({"level": "high" if low_cov > 8 else "medium",
                       "title": "%d SKUs below lead-time cover (stock-out risk)" % low_cov,
                       "source": "Inventory", "score": 0.7 + low_cov / 100})
    quakes = live.get_quakes() or []
    big = [q for q in quakes if (q.get("mag") or 0) >= 5.5]
    if big:
        alerts.append({"level": "high", "title": "M%.1f quake near supply region — %s"
                       % (big[0]["mag"], (big[0]["place"] or "")[:42]),
                       "source": "Supplier Risk (live)", "score": 0.9})
    hot = con.execute("SELECT COUNT(*) n FROM tickets WHERE severity='high' AND status='open'").fetchone()["n"]
    if hot:
        alerts.append({"level": "medium", "title": "%d high-risk support cases open" % hot,
                       "source": "Service Desk", "score": 0.65})
    alerts.sort(key=lambda x: x["score"], reverse=True)
    for x in alerts:
        x.pop("score", None)
    return {"schedule": sched, "operators": ops, "knowledgeBase": kb_queries,
            "serviceDesk": serviceDesk, "alerts": alerts[:6]}


def q_overview(con):
    o = con.execute("""SELECT SUM(planned_min) p, SUM(downtime_min) d,
                       SUM((planned_min-downtime_min)*ideal_rate) ideal,
                       SUM(good_units) g, SUM(scrap_units) s FROM oee_shifts""").fetchone()
    A = (o["p"] - o["d"]) / o["p"]
    produced = o["g"] + o["s"]
    P = clamp(produced / (o["ideal"] or 1), 0, 1)
    Q = o["g"] / (produced or 1)
    oee = round(A * P * Q * 100)

    # forecast accuracy proxy: how often the quote win_rate predicted the outcome
    qr = con.execute("""SELECT AVG(CASE WHEN (win_rate>=50)=(won=1) THEN 1.0 ELSE 0 END) acc
                        FROM quotes""").fetchone()["acc"]
    accuracy = round((qr or 0) * 100)

    uplift = con.execute("SELECT SUM(arr*upsell_score/100.0) u FROM customers").fetchone()["u"]

    # --- LIVE KPIs from public APIs ---------------------------------------
    carbon = live.get_carbon()
    fx = live.get_fx()
    quakes = live.get_quakes() or []
    disruptions = sum(1 for q in quakes if (q.get("mag") or 0) >= 5.0)
    eur = (fx or {}).get("rates", {}).get("EUR")

    kpis = [{"label": "Avg OEE", "value": "%d%%" % oee, "delta": "internal"}]
    if carbon and carbon.get("gco2") is not None:
        kpis.append({"label": "Grid Carbon", "value": "%s g" % carbon["gco2"],
                     "delta": "LIVE · %s" % carbon.get("index", "")})
    else:
        kpis.append({"label": "Forecast Accuracy", "value": "%d%%" % accuracy, "delta": "model"})
    if eur:
        kpis.append({"label": "USD / EUR", "value": "%.3f" % eur, "delta": "LIVE · ECB"})
    else:
        kpis.append({"label": "ARR Uplift", "value": "$%.1fM" % (uplift / 1000.0), "delta": "pipeline"})
    kpis.append({"label": "Active Disruptions", "value": str(disruptions),
                 "delta": "LIVE · M5+ /24h" if quakes else "offline"})
    return {"kpis": kpis}


