def clamp(v, lo, hi):
    return max(lo, min(hi, v))


def percentile(values, p):
    if not values:
        return 0.0
    s = sorted(values)
    k = (len(s) - 1) * p
    f = math.floor(k)
    c = min(f + 1, len(s) - 1)
    return s[f] + (s[c] - s[f]) * (k - f)


def histogram(values, bins=60, lo=0, hi=59):
    """Count values into `bins` buckets across [lo, hi]."""
    out = [0] * bins
    span = (hi - lo) or 1
    for v in values:
        idx = int((clamp(v, lo, hi) - lo) / span * (bins - 1) + 0.5)
        out[clamp(idx, 0, bins - 1)] += 1
    return out


def smooth(arr, passes=2):
    a = list(arr)
    for _ in range(passes):
        a = [(a[max(0, i - 1)] + a[i] + a[min(len(a) - 1, i + 1)]) / 3.0
             for i in range(len(a))]
    return a


def scale_to(arr, top=100.0):
    m = max(arr) if arr else 0
    if m <= 0:
        return [0 for _ in arr]
    return [round(v / m * top, 2) for v in arr]


