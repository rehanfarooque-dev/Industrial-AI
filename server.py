def q_revenue(con):
    # Funnel from lead journeys
    rows = {r["stage_reached"]: r["n"] for r in con.execute(
        "SELECT stage_reached, COUNT(*) n FROM leads GROUP BY stage_reached")}
    stages = ["Awareness", "Interest", "Consideration", "Loyalty"]
    # count at stage = leads that reached AT LEAST that stage
    totals = [sum(rows.get(s, 0) for s in range(k, 5)) for k in range(1, 5)]
    base = totals[0] or 1
    funnel = [{"stage": stages[i], "count": totals[i],
               "pct": round(totals[i] / base * 100),
               # conversion from the previous stage (100% for the first)
               "conv": 100 if i == 0 else round(totals[i] / (totals[i - 1] or 1) * 100)}
              for i in range(4)]

    # Margin scatter from quotes (AI-priced vs not)
    low = [{"x": r["win_rate"], "y": r["margin"]} for r in con.execute(
        "SELECT win_rate, margin FROM quotes WHERE ai_priced=0")]
    optimal = [{"x": r["win_rate"], "y": r["margin"]} for r in con.execute(
        "SELECT win_rate, margin FROM quotes WHERE ai_priced=1")]
    opt_m = [p["y"] for p in optimal]
    band = {"top": round(percentile(opt_m, 0.75)), "bottom": round(percentile(opt_m, 0.25))}

    # Demand: quarterly order volume vs an 80%-utilization target line
    qrow = {r["q"]: r["u"] for r in con.execute(
        """SELECT CASE WHEN day<91 THEN 0 WHEN day<182 THEN 1
                       WHEN day<273 THEN 2 ELSE 3 END q, SUM(qty) u
           FROM orders GROUP BY q""")}
    units = [qrow.get(i, 0) for i in range(4)]
    mu = max(units) or 1
    forecast = [round(u / mu * 95) for u in units]

    # Monetization: ARR split remote-service vs traditional
    rs = con.execute("SELECT remote_service, SUM(arr) s FROM customers GROUP BY remote_service")
    split = {r["remote_service"]: r["s"] for r in rs}
    rtot = split.get(1, 0) + split.get(0, 0)
    remote = round(split.get(1, 0) / (rtot or 1) * 100)

    # Segmentation radar: average attributes across the customer base
    seg = con.execute("""SELECT AVG(price_sensitivity) a, AVG(service_needs) b,
                         AVG(tech_adoption) c, AVG(growth_potential) d,
                         AVG(risk_profile) e FROM customers""").fetchone()
    return {
        "funnel": funnel,
        "margin": {"low": low, "optimal": optimal, "band": band},
        "demand": {"labels": ["Q1", "Q2", "Q3", "Q4"],
                   "capacity": [80, 80, 80, 80], "forecast": forecast},
        "monetization": {"remoteService": remote, "traditionalSales": 100 - remote},
        "segmentation": {
            "labels": ["Price Sensitivity", "Service Needs", "Tech Adoption",
                       "Growth Potential", "Risk Profile"],
            "values": [round(seg["a"]), round(seg["b"]), round(seg["c"]),
                       round(seg["d"]), round(seg["e"])],
        },
    }


